UINT32 *get_char_from_arabic(UINT8 charset, UINT8 character, UINT8 i)
{
   if((0xff == charset)||(character>=96)||(i>=9))
   {
       return NULL;//error value;
   }
   return((UINT32*)&ttx_arabic_font[charset][character][i]);
}

