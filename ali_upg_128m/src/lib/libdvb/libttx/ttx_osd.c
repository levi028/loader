                return((UINT32*)&ttx_g0_nations[char_nation][6][0]);
            case 0x3f:
                //nation_charidx = 7;
                return((UINT32*)&ttx_g0_nations[char_nation][7][0]);
            case 0x40:
                //nation_charidx = 8;
